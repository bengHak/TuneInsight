    static let white = UIColor.white
    static let black = UIColor.black
    static let systemBlue = UIColor.systemBlue
    static let systemGreen = UIColor.systemGreen
    static let separator = UIColor.separator
    static let label = UIColor.label
    static let clear = UIColor.clear
    static let white80 = UIColor.white.withAlphaComponent(0.8)
    static let white85 = UIColor.white.withAlphaComponent(0.85)
    static let systemBlue10 = UIColor.systemBlue.withAlphaComponent(0.1)
    static let white60 = UIColor.white.withAlphaComponent(0.6)
}
